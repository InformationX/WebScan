from pymongo import MongoClient
import json
from bson.objectid import ObjectId


class MongoDB(object):

    def __init__(self):
        self.host = '127.0.0.1'
        self.port = 27017
        self.database = 'scanner'
        self.conn = MongoClient(self.host, self.port)
        self.db = self.conn.scaner
        # self.db.authenticate('','')

    def add_vuln_info(self,taskID,name,info,notice,payload):

        temp_dic = {
            'info':info,
            'notice':notice,
            'payload':payload
        }
        coll = self.db.result
        try:
            coll.update({"_id":ObjectId(taskID)},{'$set':{"vulnerable_attack."+name: temp_dic}})
            return True
        except:
            return False

    def add_child_tasks(self,parentID,SubDomainResult):
        """
        :param parentID:
        :param SubDomainResult:
        :return:
        """
        if len(SubDomainResult) == 0:
            return None

        child_task_ids = []
        for _ in SubDomainResult.items():
            task_template = {
                "ip":_[0],
                "domain":_[1],
                "ports":"",
                "vulnerable_attack":{

                }
            }
            child_task_ids.append(str(self.db.result.insert(task_template)))

        self.db.task.update({"_id":ObjectId(parentID)},{'$push':{'ChildTaskID':{'$each':child_task_ids}}})

    def add_open_ports(self, taskID, result):
        ports_result = dict()
        result = json.loads(result)
        if result:
            for _ in result.items():
                ports_result = _[1]
            print(ports_result)
            coll = self.db.result
            try:
                coll.update({"_id":ObjectId(taskID)}, {'$set': {"ports" : ports_result}})
                return True
            except Exception as e:
                print(e)
                return False
        else:
            print("no")




